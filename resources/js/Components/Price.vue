<template>
    <span>
        {{ formattedPrice }}
    </span>
</template>

<script setup>
    import { computed } from 'vue'

    const props = defineProps({
        price: [Number, String],
    })

    const formattedPrice = computed(() => {
        return new Intl.NumberFormat('en-US', {
            style: 'currency',
            currency: 'USD',
            maximumFractionDigits: 0,
        }).format(props.price)
    })
</script>
